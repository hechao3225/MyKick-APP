public class Errorlj23 {
    public String model;
    public String message;

    public Errorlj23() {
        super();
    }

    public Errorlj23(String model, String message) {
        super();
        this.model = model;
        this.message = message;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
